def _create_model_issue(item: dict, existing_titles: set[str]) -> dict | None:
    """Create a GitHub issue recommending evaluation of a trending GGUF model."""
    title = f"[Pipeline] Evaluate model: {item.get('title', 'Unknown')}"
    if title in existing_titles:
        log.info(f"Skipping duplicate model issue: {title}")
        return None

    body = textwrap.dedent(f"""\
        ## Auto-generated by PyMasters AI Intelligence Pipeline

        **Source:** {item.get('source', 'unknown')}
        **Original:** [{item.get('title', '')}]({item.get('url', '')})
        **Homie Score:** {item.get('homie_score', 0)}/10

        ### Model Recommendation
        {item.get('opportunity', 'N/A')}

        ### Description
        {item.get('description', 'N/A')[:800]}

        ### Suggested Evaluation Steps
        1. Download the GGUF variant (Q4_K_M or Q5_K_M recommended)
        2. Test inference speed on target hardware
        3. Compare quality against current default model
        4. Update `homie.config.yaml` if the model is an improvement
        5. Update `model_evolution` config if applicable

        ### Current Homie Model Config
        - Backend: gguf
        - Context length: 65536
        - Current model path configured in `homie.config.yaml`

        ---
        *This issue was automatically created by the daily AI intelligence pipeline.*
    """)

    try:
        _ensure_label()
        result = _gh(
            "issue", "create",
            "--repo", HOMIE_REPO,
            "--title", title,
            "--body", body,
            "--label", "pipeline-generated",
        )
        if result.returncode == 0:
            url = result.stdout.strip()
            log.info(f"Created model issue: {url}")
            return {"title": title, "url": url, "type": "model_issue", "item": item}
        else:
            log.error(f"Model issue creation failed: {result.stderr}")
    except Exception as exc:
        log.error(f"Error creating model issue: {exc}")

    return None


