def evolve_homie(items: list[dict]) -> list[dict]:
    """Process high-scoring Homie items and create PRs/issues.

    Returns a list of created PRs and issues (dicts with 'title', 'url', 'type').
    """
    candidates = [
        item for item in items
        if item.get("homie_score", 0) >= RELEVANCE_THRESHOLD_HOMIE
        and item.get("product") in ("homie", "both")
    ]

    if not candidates:
        log.info("No Homie evolution candidates found.")
        return []

    log.info(f"Found {len(candidates)} Homie evolution candidates.")
    _ensure_label()

    existing_titles = _get_existing_titles()
    homie_context = _read_homie_source_context()

    results: list[dict] = []
    prs_created = 0

    for item in candidates:
        category = _classify_item(item)
        log.info(f"Processing [{category}]: {item.get('title', '')[:60]}")

        if category == "plugin" and prs_created < MAX_PRS_PER_RUN:
            pr = _create_plugin_pr(item, homie_context, existing_titles)
            if pr:
                results.append(pr)
                existing_titles.add(pr["title"])
                prs_created += 1
        elif category == "model":
            issue = _create_model_issue(item, existing_titles)
            if issue:
                results.append(issue)
                existing_titles.add(issue["title"])
        else:
            issue = _create_capability_issue(item, existing_titles)
            if issue:
                results.append(issue)
                existing_titles.add(issue["title"])

    log.info(f"Homie evolution complete: {len(results)} items created ({prs_created} PRs).")
    return results
