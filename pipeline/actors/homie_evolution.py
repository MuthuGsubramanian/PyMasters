_PLUGIN_GEN_SYSTEM = textwrap.dedent("""\
    You are a senior Python developer contributing to Homie, a cross-platform local AI assistant.
    Homie uses: llama.cpp (GGUF inference), ChromaDB (RAG), Whisper (STT), Piper (TTS).
    It is a LOCAL desktop/mobile app, NOT a web service.

    When asked to generate a plugin, produce a SINGLE Python file that:
    - Has a module-level docstring explaining the plugin
    - Contains a class inheriting from a base if one exists, or a standalone class
    - Includes an ``activate()`` / ``deactivate()`` pair or ``register()`` function
    - Has clear type hints and docstrings
    - Is safe to run locally (no network calls unless opt-in)
    - Follows existing Homie code conventions shown in the context
""")


def _generate_plugin_code(item: dict, homie_context: str) -> tuple[str, str, str]:
    """Use Claude to generate plugin code. Returns (filename, code, description)."""
    client = Anthropic()

    prompt = textwrap.dedent(f"""\
        ## Task
        Generate a Homie plugin based on this pipeline discovery:

        **Title:** {item.get('title', '')}
        **Source:** {item.get('source', '')}
        **URL:** {item.get('url', '')}
        **Opportunity:** {item.get('opportunity', '')}
        **Description:** {item.get('description', '')[:500]}

        ## Existing Homie Codebase Context
        {homie_context}

        ## Output Format
        Respond with ONLY a JSON object (no markdown fences):
        {{
            "filename": "plugin_name.py",
            "code": "... full Python source ...",
            "description": "A one-paragraph description of what this plugin does and how it integrates with Homie."
        }}
    """)

    response = client.messages.create(
        model="claude-sonnet-4-20250514",
        max_tokens=4096,
        system=_PLUGIN_GEN_SYSTEM,
        messages=[{"role": "user", "content": prompt}],
    )

    text = response.content[0].text.strip()
    # Handle markdown code blocks
    if text.startswith("```"):
        lines = text.split("\n")
        text = "\n".join(lines[1:-1])

    data = json.loads(text)
    return data["filename"], data["code"], data["description"]


def _create_plugin_pr(item: dict, homie_context: str, existing_titles: set[str]) -> dict | None:
    """Generate a plugin and open a PR on the Homie repo. Returns PR info or None."""
    title = f"[Pipeline] Add plugin: {item.get('opportunity', item.get('title', ''))}"
    if title in existing_titles:
        log.info(f"Skipping duplicate PR: {title}")
        return None

    log.info(f"Generating plugin for: {item.get('title', '')}")
    try:
        filename, code, description = _generate_plugin_code(item, homie_context)
    except Exception as exc:
        log.error(f"Plugin code generation failed: {exc}")
        return None

    # Sanitize filename
    filename = filename.replace("/", "_").replace("\\", "_")
    if not filename.endswith(".py"):
        filename += ".py"

    branch_name = f"pipeline/plugin-{filename.replace('.py', '').replace('_', '-')}"

    # Use a temporary directory to stage the PR
    with tempfile.TemporaryDirectory() as tmpdir:
        try:
            # Clone the repo shallowly
            clone_result = subprocess.run(
                ["gh", "repo", "clone", HOMIE_REPO, tmpdir, "--", "--depth=1"],
                capture_output=True, text=True, timeout=60,
            )
            if clone_result.returncode != 0:
                log.error(f"Clone failed: {clone_result.stderr}")
                return None

            repo_dir = Path(tmpdir)

            # Create branch
            subprocess.run(
                ["git", "checkout", "-b", branch_name],
                cwd=repo_dir, capture_output=True, text=True, timeout=10,
            )

            # Write plugin file
            plugin_dir = repo_dir / "src" / "homie" / "plugins"
            plugin_dir.mkdir(parents=True, exist_ok=True)

            # Ensure __init__.py exists
            init_file = plugin_dir / "__init__.py"
            if not init_file.exists():
                init_file.write_text('"""Homie plugins package."""\n', encoding="utf-8")

            plugin_path = plugin_dir / filename
            plugin_path.write_text(code, encoding="utf-8")

            # Commit
            subprocess.run(["git", "add", "."], cwd=repo_dir, capture_output=True, text=True)
            commit_msg = f"feat(plugin): add {filename.replace('.py', '')} plugin\n\nGenerated by PyMasters AI Intelligence Pipeline.\nSource: {item.get('url', 'N/A')}"
            subprocess.run(
                ["git", "commit", "-m", commit_msg],
                cwd=repo_dir, capture_output=True, text=True, timeout=15,
            )

            # Push
            push_result = subprocess.run(
                ["git", "push", "origin", branch_name],
                cwd=repo_dir, capture_output=True, text=True, timeout=60,
            )
            if push_result.returncode != 0:
                log.error(f"Push failed: {push_result.stderr}")
                return None

            # Create PR
            body = textwrap.dedent(f"""\
                ## Auto-generated by PyMasters AI Intelligence Pipeline

                **Source:** {item.get('source', 'unknown')}
                **Original:** [{item.get('title', '')}]({item.get('url', '')})
                **Homie Score:** {item.get('homie_score', 0)}/10

                ### What this plugin does
                {description}

                ### Plugin file
                `src/homie/plugins/{filename}`

                ### Review checklist
                - [ ] Code quality and safety review
                - [ ] Test locally
                - [ ] Check for security concerns
                - [ ] Verify integration with Homie config

                ---
                *This PR was automatically created by the daily AI intelligence pipeline.*
            """)

            pr_result = subprocess.run(
                [
                    "gh", "pr", "create",
                    "--repo", HOMIE_REPO,
                    "--head", branch_name,
                    "--title", title,
                    "--body", body,
                    "--label", "pipeline-generated",
                ],
                cwd=repo_dir,
                capture_output=True, text=True, timeout=30,
            )

            if pr_result.returncode == 0:
                pr_url = pr_result.stdout.strip()
                log.info(f"Created PR: {pr_url}")
                return {"title": title, "url": pr_url, "type": "plugin_pr", "item": item}
            else:
                log.error(f"PR creation failed: {pr_result.stderr}")
                return None

        except Exception as exc:
            log.error(f"Plugin PR workflow failed: {exc}")
            return None


