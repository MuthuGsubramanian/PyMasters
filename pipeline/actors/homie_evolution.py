# ---------------------------------------------------------------------------
# Classification helpers
# ---------------------------------------------------------------------------

_MODEL_KEYWORDS = {"gguf", "quantized", "model", "llm", "weights", "fine-tune", "finetune"}
_PLUGIN_KEYWORDS = {"plugin", "library", "tool", "integration", "api", "sdk", "package"}


def _classify_item(item: dict) -> str:
    """Classify an item as 'plugin', 'model', or 'capability'."""
    text = " ".join([
        item.get("title", ""),
        item.get("opportunity", ""),
        item.get("description", ""),
    ]).lower()

    # Model recommendations take priority for HuggingFace GGUF items
    if item.get("source") == "huggingface" and "gguf" in text:
        return "model"
    if any(kw in text for kw in _MODEL_KEYWORDS) and item.get("source") == "huggingface":
        return "model"

    # Plugin opportunities
    if any(kw in text for kw in _PLUGIN_KEYWORDS):
        return "plugin"

    return "capability"


