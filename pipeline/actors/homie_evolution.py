"""Auto-generate Homie improvements based on pipeline discoveries.

Handles three categories:
1. Plugin opportunities  - generate plugin code and create PRs on MuthuGsubramanian/Homie
2. Model recommendations - create issues when new GGUF models are trending
3. Capability improvements - create detailed issues with implementation suggestions
"""

import json
import os
import subprocess
import tempfile
import textwrap
from pathlib import Path

from anthropic import Anthropic

from pipeline.config import HOMIE_REPO, RELEVANCE_THRESHOLD_HOMIE
from pipeline.utils.logger import get_logger

log = get_logger("actor.homie_evolution")

MAX_PRS_PER_RUN = 2

# Path to the Homie repo (for reading plugin structure).  Resolved relative
# to this file's location so it works in CI and locally.
_HOMIE_SRC = Path(os.getenv(
    "HOMIE_SRC",
    str(Path(__file__).resolve().parents[2].parent / "Homie" / "src" / "homie"),
))

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _gh(*args: str, timeout: int = 30) -> subprocess.CompletedProcess:
    """Run a ``gh`` CLI command and return the result."""
    return subprocess.run(
        ["gh", *args],
        capture_output=True,
        text=True,
        timeout=timeout,
    )


def _ensure_label() -> None:
    """Ensure the pipeline-generated label exists on the Homie repo."""
    _gh(
        "label", "create", "pipeline-generated",
        "--repo", HOMIE_REPO,
        "--color", "0E8A16",
        "--description", "Auto-generated by AI pipeline",
        "--force",
    )


def _get_existing_titles() -> set[str]:
    """Return titles of existing pipeline-generated issues and PRs."""
    titles: set[str] = set()
    for state in ("open", "closed"):
        try:
            result = _gh(
                "issue", "list",
                "--repo", HOMIE_REPO,
                "--label", "pipeline-generated",
                "--state", state,
                "--limit", "100",
                "--json", "title",
            )
            if result.returncode == 0:
                for entry in json.loads(result.stdout):
                    titles.add(entry["title"])
        except Exception as exc:
            log.warning(f"Could not fetch existing issues ({state}): {exc}")

    # Also check open PRs
    try:
        result = _gh(
            "pr", "list",
            "--repo", HOMIE_REPO,
            "--label", "pipeline-generated",
            "--state", "all",
            "--limit", "100",
            "--json", "title",
        )
        if result.returncode == 0:
            for entry in json.loads(result.stdout):
                titles.add(entry["title"])
    except Exception as exc:
        log.warning(f"Could not fetch existing PRs: {exc}")

    return titles


def _read_homie_source_context() -> str:
    """Read key Homie source files to give Claude context about the codebase."""
    context_parts: list[str] = []
    files_to_read = [
        _HOMIE_SRC / "__init__.py",
        _HOMIE_SRC / "config.py",
        _HOMIE_SRC / "homie.py",
    ]
    # Also scan for any existing plugin-like files
    if _HOMIE_SRC.exists():
        for py_file in sorted(_HOMIE_SRC.rglob("*.py")):
            rel = py_file.relative_to(_HOMIE_SRC)
            # Skip __pycache__
            if "__pycache__" in str(rel):
                continue
            files_to_read.append(py_file)

    seen: set[str] = set()
    for fpath in files_to_read:
        key = str(fpath.resolve())
        if key in seen or not fpath.exists():
            continue
        seen.add(key)
        try:
            content = fpath.read_text(encoding="utf-8", errors="replace")
            # Truncate large files
            if len(content) > 3000:
                content = content[:3000] + "\n# ... (truncated)"
            context_parts.append(f"### {fpath.relative_to(_HOMIE_SRC.parent)}\n```python\n{content}\n```")
        except Exception:
            pass

    return "\n\n".join(context_parts[:15])  # cap at 15 files


