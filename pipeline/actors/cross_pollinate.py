# Public API
# ---------------------------------------------------------------------------

def cross_pollinate(items: list[dict]) -> list[dict]:
    """Create cross-pollination issues between Homie and PyMasters.

    Items that score high for BOTH products get issues on both repos.
    Items scoring high for one product get a suggestion on the other.

    Returns list of created issues.
    """
    results: list[dict] = []

    # Items relevant to both products
    both_items = [
        item for item in items
        if item.get("product") == "both"
        and item.get("homie_score", 0) >= RELEVANCE_THRESHOLD_HOMIE
        and item.get("pymasters_score", 0) >= RELEVANCE_THRESHOLD_PYMASTERS
    ]

    # Items primarily for PyMasters but potentially useful for Homie
    pymasters_to_homie = [
        item for item in items
        if item.get("product") == "pymasters"
        and item.get("pymasters_score", 0) >= RELEVANCE_THRESHOLD_PYMASTERS
        and item.get("homie_score", 0) >= 4  # moderate Homie relevance
    ]

    # Items primarily for Homie but potentially good tutorial material
    homie_to_pymasters = [
        item for item in items
        if item.get("product") == "homie"
        and item.get("homie_score", 0) >= RELEVANCE_THRESHOLD_HOMIE
        and item.get("pymasters_score", 0) >= 4  # moderate PyMasters relevance
    ]

    candidates = both_items + pymasters_to_homie + homie_to_pymasters
    if not candidates:
        log.info("No cross-pollination candidates found.")
        return results

    log.info(f"Found {len(candidates)} cross-pollination candidates "
             f"(both={len(both_items)}, pm->homie={len(pymasters_to_homie)}, "
             f"homie->pm={len(homie_to_pymasters)}).")

    homie_existing = _get_existing_titles(HOMIE_REPO)
    pymasters_existing = _get_existing_titles(PYMASTERS_REPO)

    # Process "both" items — create issues on both repos
    for item in both_items[:3]:  # cap at 3
        issue = _create_homie_issue_from_pymasters(item, homie_existing)
        if issue:
            results.append(issue)
            homie_existing.add(issue["title"])

        issue = _create_pymasters_issue_from_homie(item, pymasters_existing)
        if issue:
            results.append(issue)
            pymasters_existing.add(issue["title"])

    # PyMasters discoveries -> Homie suggestions
    for item in pymasters_to_homie[:2]:  # cap at 2
        issue = _create_homie_issue_from_pymasters(item, homie_existing)
        if issue:
            results.append(issue)
            homie_existing.add(issue["title"])

    # Homie discoveries -> PyMasters tutorial suggestions
    for item in homie_to_pymasters[:2]:  # cap at 2
        issue = _create_pymasters_issue_from_homie(item, pymasters_existing)
        if issue:
            results.append(issue)
            pymasters_existing.add(issue["title"])

    log.info(f"Cross-pollination complete: {len(results)} issues created.")
    return results
