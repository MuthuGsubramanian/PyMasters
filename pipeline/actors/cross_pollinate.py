"""Cross-pollination between Homie and PyMasters.

Creates GitHub issues that link discoveries between the two products:
1. PyMasters lesson about a technique -> Homie issue suggesting integration
2. Homie capability added -> PyMasters issue suggesting a tutorial
"""

import json
import subprocess

from pipeline.config import (
    HOMIE_REPO,
    PYMASTERS_REPO,
    RELEVANCE_THRESHOLD_HOMIE,
    RELEVANCE_THRESHOLD_PYMASTERS,
)
from pipeline.utils.logger import get_logger

log = get_logger("actor.cross_pollinate")


def _gh(*args: str, timeout: int = 30) -> subprocess.CompletedProcess:
    return subprocess.run(
        ["gh", *args],
        capture_output=True,
        text=True,
        timeout=timeout,
    )


def _ensure_label(repo: str) -> None:
    _gh(
        "label", "create", "pipeline-generated",
        "--repo", repo,
        "--color", "0E8A16",
        "--description", "Auto-generated by AI pipeline",
        "--force",
    )


def _get_existing_titles(repo: str) -> set[str]:
    titles: set[str] = set()
    try:
        result = _gh(
            "issue", "list",
            "--repo", repo,
            "--label", "pipeline-generated",
            "--state", "all",
            "--limit", "100",
            "--json", "title",
        )
        if result.returncode == 0:
            for entry in json.loads(result.stdout):
                titles.add(entry["title"])
    except Exception as exc:
        log.warning(f"Could not fetch existing issues for {repo}: {exc}")
    return titles


def _create_homie_issue_from_pymasters(item: dict, existing: set[str]) -> dict | None:
    """When a PyMasters-relevant item is found, suggest Homie integration."""
    title = f"[Cross-pollinate] Integrate technique from: {item.get('title', 'Unknown')[:80]}"
    if title in existing:
        log.info(f"Skipping duplicate cross-pollination issue: {title}")
        return None

    body = f"""## Cross-pollination: PyMasters -> Homie

**Source:** {item.get('source', 'unknown')}
**Original:** [{item.get('title', '')}]({item.get('url', '')})
**PyMasters Score:** {item.get('pymasters_score', 0)}/10
**Homie Score:** {item.get('homie_score', 0)}/10

### Context
This item scored high for PyMasters content. It may also be applicable as a Homie feature
or plugin. This issue is a suggestion to evaluate whether the technique can be integrated
into Homie's local-first architecture.

### Opportunity
{item.get('opportunity', 'N/A')}

### Description
{item.get('description', 'N/A')[:500]}

### Suggested Next Steps
- Review the technique for local applicability (no cloud dependency)
- Check resource requirements for desktop and mobile targets
- If feasible, create a Homie plugin or core feature enhancement

---
*Cross-pollination issue created by the PyMasters AI Intelligence Pipeline.*
"""

    try:
        _ensure_label(HOMIE_REPO)
        result = _gh(
            "issue", "create",
            "--repo", HOMIE_REPO,
            "--title", title,
            "--body", body,
            "--label", "pipeline-generated",
        )
        if result.returncode == 0:
            url = result.stdout.strip()
            log.info(f"Created cross-pollination issue (Homie): {url}")
            return {"title": title, "url": url, "type": "cross_pymasters_to_homie", "item": item}
        else:
            log.error(f"Failed to create Homie cross-pollination issue: {result.stderr}")
    except Exception as exc:
        log.error(f"Error creating cross-pollination issue: {exc}")

    return None


